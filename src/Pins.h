// pins.h
#ifndef PINS_H
#define PINS_H

// =============================================================================
// MOTEURS ESC (PWM)
// =============================================================================
//        / 1 \         |----------------|
//       /  |  \        | Moteurs        |
//      |3-----4|       |      Position  |
//       \__2__/        |----------------|
// =============================================================================
#define PIN_ESC1        PA8     
#define PIN_ESC2        PA9     
#define PIN_ESC3        PA10    
#define PIN_ESC4        PA11    

// =============================================================================
// COMMUNICATION RS485
// =============================================================================
#define PIN_RS485_TX    PA2
#define PIN_RS485_RX    PA3
#define PIN_RS485_DE    PB0     // Driver Enable / Receiver Enable

// =============================================================================
// IMU BNO08x (I2C)
// =============================================================================
#define PIN_IMU_SCL     PB6
#define PIN_IMU_SDA     PB7

// =============================================================================
// Capteur de pression MS5837 (même bus I2C que l'IMU)
// =============================================================================
// Pas de pin supplémentaire (même SCL/SDA)

// =============================================================================
// Pompe péristaltique (DRV8871)
// =============================================================================
#define PIN_DRV_IN1     PA0
#define PIN_DRV_IN2     PA1
// Optionnel : PIN_DRV_SLEEP   (si tu veux pouvoir couper le driver)

// =============================================================================
// Autres (exemples)
// =============================================================================
#define PIN_LED_DEBUG   PC13    // LED intégrée Black Pill
#define PIN_ADC_VBAT    PA1     // Pour mesurer la tension batterie

// =============================================================================
// Constantes utiles
// =============================================================================
#define ESC_NEUTRAL     1500    // µs
#define ESC_MIN         1000
#define ESC_MAX         2000

#endif // PINS_H